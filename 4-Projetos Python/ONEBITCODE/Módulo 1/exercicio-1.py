# Exercício Numero 1

# Antecessor e Sucessor
numero = int(input("Digite um número\n"))

print(f"Antecessor do número: {numero -1} | Sucessor do número: {numero +1}")

# Media Aritmetica
nota1 = float(input("Digite a nota 1\n"))
nota2 = float(input("Digite a nota 2\n"))
nota3 = float(input("Digite a nota 3\n"))
nota4 = float(input("Digite a nota 4\n"))

average = (nota1 + nota2 + nota3 + nota4) /4

print(f"Médias das notas é: {average:.2f}")
